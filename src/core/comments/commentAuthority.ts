/**
 * Разграничение «кто отвечает в обсуждении» между общим ботом и ботом клиента
 * (Шаг 14b-bis-4) — ЧИСТАЯ логика, без БД/Telegram/реестра.
 *
 * Когда бот клиента добавлен в группу обсуждения СВОЕГО канала, апдейт-коммент
 * получают ОБА бота (общий + клиента). Без разграничения оба прогонят конвейер
 * (модерация → триггер → AI-ответ) и подписчик увидит два ответа. Здесь — единое
 * правило «пускать ли этот коммент в мои хендлеры», по нему обе стороны молчат там,
 * где отвечать должна другая.
 */

/** Факты о принадлежности коммента, нужные для решения «отвечать/молчать». */
export interface CommentAuthorityInput {
  /**
   * Telegram-id владельца бота, который сейчас обрабатывает апдейт. `undefined` —
   * это ОБЩИЙ бот (обслуживает всех зарегистрированных владельцев).
   */
  clientOwnerUserId: number | undefined;
  /**
   * Telegram-id владельца канала, к которому отнесён коммент. `null` — канал без
   * владельца (бесхозный) либо его владельца нельзя однозначно определить.
   */
  channelOwnerTelegramId: number | null;
  /**
   * У владельца канала поднят свой бот — значит в обсуждении ответит ОН. Считается
   * по реестру ботов клиентов (бот реально работает), а не по флагу в БД: если бот
   * клиента упал, общий должен подхватить, а не оставить обсуждение без ответа.
   */
  channelOwnerHasClientBot: boolean;
}

/**
 * Решает, должен ли ЭТОТ бот обрабатывать коммент указанного канала.
 *
 * - Бот клиента отвечает ТОЛЬКО в обсуждении СВОИХ каналов. Резолв канала имеет
 *   фолбэк на «первый активный канал списка»: для бота клиента он может подсунуть
 *   чужой канал — там владелец не совпадёт, и бот промолчит (не утащит ответ в
 *   чужую группу).
 * - Общий бот молчит в обсуждении каналов, у владельцев которых поднят свой бот
 *   (тот и ответит), иначе ответили бы оба. Везде, где своего бота нет, общий
 *   работает как раньше.
 */
export function shouldHandleComment(input: CommentAuthorityInput): boolean {
  const { clientOwnerUserId, channelOwnerTelegramId, channelOwnerHasClientBot } =
    input;

  if (clientOwnerUserId !== undefined) {
    return channelOwnerTelegramId === clientOwnerUserId;
  }

  return !channelOwnerHasClientBot;
}
