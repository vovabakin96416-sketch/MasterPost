/**
 * Сопоставление текста комментария со словами-триггерами канала.
 *
 * ЧИСТАЯ логика (без Telegram/БД): на вход — текст и список слов из
 * `Channel.triggerWords`, на выход — совпавшее слово (оно же ключ пула `TextPool`)
 * или `null`. Никакой тематики в коде: слова приходят из конфига канала, поэтому
 * один и тот же код обслуживает таро-канал и любой другой.
 *
 * Замена per-word regex Python-бота (`KARTA/KOFE/RUNA`) на общую нормализацию:
 * нормализуем одинаково и текст, и слово, затем сравниваем на равенство.
 */

/**
 * Приводит строку к каноничной форме для сравнения:
 * - нижний регистр;
 * - `ё → е` (как в KOFE: `[её]`);
 * - любые не-буквы/не-цифры (пробелы, знаки, эмодзи) по краям и внутри → пробел;
 * - схлопывание подряд идущих одинаковых символов (`кааарта → карта`).
 *
 * Применяется к ОБЕИМ сторонам сравнения, поэтому слова с легитимными двойными
 * буквами тоже матчатся корректно (обе стороны схлопываются одинаково).
 */
export function normalizeTriggerText(input: string): string {
  return input
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^\p{L}\p{N}]+/gu, " ")
    .trim()
    .replace(/(.)\1+/gu, "$1");
}

/**
 * Возвращает совпавшее слово-триггер (как оно задано в конфиге канала) либо
 * `null`. Совпавшее слово используется и как ключ пула, и как ключ кулдауна.
 */
export function matchTrigger(
  text: string,
  triggerWords: readonly string[],
): string | null {
  const normalized = normalizeTriggerText(text);
  if (normalized === "") {
    return null;
  }
  for (const word of triggerWords) {
    if (normalizeTriggerText(word) === normalized) {
      return word;
    }
  }
  return null;
}
