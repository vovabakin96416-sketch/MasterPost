/**
 * Типы медиа-слоя (Шаг 6a). ЧИСТЫЕ (без grammY/БД/сети): описывают «что за фото»
 * и «какой тариф», но не умеют ни ходить в сеть, ни слать в Telegram.
 *
 * Две версии продукта живут в `MediaTier`: бесплатная (`free`, сток Pexels) и
 * платная (`paid`, AI-генерация — движок на Шаге 10). Сейчас обе сводятся к подбору
 * фото; платный провайдер пока заглушка с откатом на сток.
 */

/** Тариф канала: бесплатная (сток) или платная (генерация) версия. */
export type MediaTier = "free" | "paid";

/**
 * Ссылка на фото для отправки. Дискриминированное объединение источников:
 * - `url` — внешний URL (например, картинка Pexels);
 * - `fileId` — Telegram `file_id` (своё загруженное фото; переиспользуется без ре-аплоада);
 * - `path` — локальный файл из контент-плана (оборачивается в `InputFile` на границе).
 *
 * grammY шлёт `url` и `fileId` как обычную строку, `path` — как `InputFile`.
 * Преобразование к grammY-входу делает Telegram-слой (`postingService`), чтобы
 * этот модуль оставался свободным от зависимостей.
 */
export type PhotoRef =
  | { readonly kind: "url"; readonly url: string }
  | { readonly kind: "fileId"; readonly fileId: string }
  | { readonly kind: "path"; readonly path: string };
