/**
 * Вовлечённость поста (Шаг 12a) — ЧИСТАЯ логика (без Telegram/БД).
 *
 * Сырые просмотры обманывают: у канала на 400 подписчиков «лучший по просмотрам» —
 * это часто просто пост, до которого дошло больше людей, а не тот, что зацепил. Поэтому
 * основная метрика анализа — ERR (engagement rate by reach): доля увидевших, кто
 * среагировал (реакция или коммент). Это база для «что зашло / что провалилось» (12c).
 */

/** Минимум полей поста, из которых считается вовлечённость (совместим с `PostMetricInput`). */
export interface EngagementLike {
  readonly views: number;
  readonly reactions: number;
  readonly replies: number;
}

/**
 * ERR = (реакции + комменты) / просмотры. Возвращает долю 0..1 (форматирование в %
 * — задача слоя отчёта). Просмотры ≤ 0 → 0 (безопасное деление: у свежего/скрытого
 * поста просмотров может ещё не быть, деления на ноль быть не должно).
 */
export function engagementRate(m: EngagementLike): number {
  if (m.views <= 0) {
    return 0;
  }
  return (m.reactions + m.replies) / m.views;
}
