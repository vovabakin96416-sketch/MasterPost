/**
 * Аналитика канала, часть «напоминалка о конце контента» (Шаг 7a) — ЧИСТАЯ логика.
 *
 * Порт `analytics.py:check_content_ending`: контент-план длится 4 недели и идёт по
 * кругу (см. core/schedule/resolveCampaignDay.ts). Когда наступает последняя, 4-я
 * неделя, владельцу стоит напомнить залить контент на новый месяц, пока цикл не
 * начался заново. Здесь — единственное правило; расчёт текущей недели и отправка
 * сообщения живут выше (сервис), чтобы core не зависел от БД/Telegram.
 */

/** Последняя неделя 4-недельного контент-плана — пора готовить новый месяц. */
export const LAST_CAMPAIGN_WEEK = 4;

/** Нужно ли напомнить о конце контента: да, если идёт последняя неделя плана. */
export function shouldWarnContentEnding(week: number): boolean {
  return week === LAST_CAMPAIGN_WEEK;
}
